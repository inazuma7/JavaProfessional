package Day3;

public class valueCompare {
	public static void main (String [] args) {
		int a = 5;
		int b = 10;
		
		System.out.println(a < b);
		System.out.println(a > 100);
		System.out.println(a <= 5);
		System.out.println(b == 2 * a);
		System.out.println(b != 2 * a);
		
		
	}

}
