package Day3;

public class CharLiteral {
	public static void main (String [] args) {
		
		char ch = 'a';
		System.out.printf("The code of %c is: %d%n", ch, (int) ch);
		
		ch = '\u006F';
		System.out.printf("The code of %c is: %d%n", ch, (int) ch);
		
		ch = '\u8449';
		System.out.printf("The code of %c is: %d%n", ch, (int) ch);
		
		ch = '\'';
		System.out.printf("The code of %c is: %d%n", ch, (int) ch);
		
		ch = '\\';
		System.out.printf("The code of %c is: %d%n", ch, (int) ch);
		
		ch = '\n';
		System.out.printf("The code of %c is: %d%n", ch, (int) ch);
		
		ch = '\t';
		System.out.printf("The code of %c is: %d%n", ch, (int) ch);
		
		//ch = "a";
		//System.out.printf("The code of %c is: %d%n", ch, (int) ch);
		
		
		
		
		
		
		
		
		
		
	}

}
