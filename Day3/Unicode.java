public class Unicode {
	public static void main (String [] args) {
		
		char ch = 'a';
		System.out.printf("The code of %c is: %d%n", ch, (int) ch);
		ch = 'b';
		System.out.printf("The code of %c is: %d%n", ch, (int) ch);
		ch = 'A';
		System.out.printf("The code of %c is: %d%n", ch, (int) ch);
		ch = 'Ñ‰';
		System.out.printf("The code of %c is: %d%n", ch, (int) ch);
		
		
	}

}
